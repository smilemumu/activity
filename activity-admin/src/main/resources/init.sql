DELIMITER //
CREATE PROCEDURE init_claim_records()
BEGIN
  DECLARE i INT DEFAULT 1;
  DECLARE r_name VARCHAR(100);
  DECLARE r_code VARCHAR(50);
  WHILE i <= 110 DO
    SET r_name = CASE (i % 5)
      WHEN 1 THEN '新人积分100'
      WHEN 2 THEN '优惠券30元'
      WHEN 3 THEN '返现50元'
      WHEN 4 THEN '提额券200元'
      ELSE '京东卡100元'
    END;
    SET r_code = CASE (i % 5)
      WHEN 1 THEN CONCAT('RC-REG-', LPAD(i, 3, '0'))
      WHEN 2 THEN CONCAT('RC-AUTH-', LPAD(i, 3, '0'))
      WHEN 3 THEN CONCAT('RC-LOAN-', LPAD(i, 3, '0'))
      WHEN 4 THEN CONCAT('RC-LIMIT-', LPAD(i, 3, '0'))
      ELSE CONCAT('RC-LADDER-', LPAD(i, 3, '0'))
    END;
    INSERT INTO REWARD_CLAIM_RECORD (ACTIVITY_ID, CUSTOMER_ID, PHONE, REAL_NAME, REWARD_CODE, REWARD_NAME, CLAIM_TIME)
    VALUES (
      1,
      CONCAT('CUST', LPAD(i, 5, '0')),
      CONCAT('138', LPAD(FLOOR(10000000 + RAND() * 90000000), 8, '0')),
      CONCAT('用户', i),
      r_code,
      r_name,
      DATE_ADD('2026-03-01 08:00:00', INTERVAL FLOOR(RAND() * 80 * 24 * 60) MINUTE)
    );
    SET i = i + 1;
  END WHILE;
END//
DELIMITER ;
CALL init_claim_records();
