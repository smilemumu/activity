-- 活动配置后台 建表脚本
-- 数据库: MySQL 5.7

-- 1. 活动主表
CREATE TABLE ACTIVITY (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACTIVITY_TYPE VARCHAR(30) NOT NULL,
    TITLE VARCHAR(100) NOT NULL,
    DESCRIPTION VARCHAR(500),
    START_TIME DATETIME NOT NULL,
    END_TIME DATETIME NOT NULL,
    COVER_IMAGE VARCHAR(500),
    STATUS VARCHAR(20) DEFAULT 'DRAFT',
    CREATED_BY VARCHAR(50),
    CREATED_TIME DATETIME DEFAULT CURRENT_TIMESTAMP,
    UPDATED_TIME DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 2. 活动-归属机构关联
CREATE TABLE ACTIVITY_ORG_REL (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACTIVITY_ID BIGINT NOT NULL,
    ORG_CODE VARCHAR(20) NOT NULL,
    ORG_NAME VARCHAR(100)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 3. 活动-归属部门关联
CREATE TABLE ACTIVITY_DEPT_REL (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACTIVITY_ID BIGINT NOT NULL,
    DEPT_NAME VARCHAR(100),
    CONTACT_NAME VARCHAR(50)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 4. 页面配置
CREATE TABLE PAGE_CONFIG (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACTIVITY_ID BIGINT NOT NULL,
    CONFIG_TYPE VARCHAR(30) NOT NULL,
    CONFIG_KEY VARCHAR(100),
    CONFIG_VALUE LONGTEXT,
    WIDTH INT,
    HEIGHT INT,
    SORT_ORDER INT DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 5. 规则基础配置
CREATE TABLE RULE_CONFIG (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACTIVITY_ID BIGINT NOT NULL UNIQUE,
    HAS_REWARD CHAR(1) DEFAULT 'N',
    REWARD_DELIVERY_TYPE VARCHAR(20),
    RULE_TYPE VARCHAR(30) DEFAULT 'REGISTER',
    TOTAL_CLAIM_LIMIT_ENABLED CHAR(1) DEFAULT 'N',
    TOTAL_CLAIM_LIMIT INT,
    REGION_LIMIT_ENABLED CHAR(1) DEFAULT 'N',
    MUTUAL_EXCLUSIVE_ENABLED CHAR(1) DEFAULT 'N',
    MUTUAL_EXCLUSIVE_ACTIVITY_ID BIGINT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 6. 活动-产品关联
CREATE TABLE ACTIVITY_PRODUCT_REL (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACTIVITY_ID BIGINT NOT NULL,
    PRODUCT_CODE VARCHAR(20) NOT NULL,
    PRODUCT_NAME VARCHAR(50)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 7. 参与条件配置
CREATE TABLE PARTICIPATION_CONDITION (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACTIVITY_ID BIGINT NOT NULL,
    CONDITION_TYPE VARCHAR(30) NOT NULL,
    PRODUCT_FILTER LONGTEXT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 8. 奖励配置
CREATE TABLE REWARD_CONFIG (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACTIVITY_ID BIGINT NOT NULL,
    REWARD_TYPE VARCHAR(30) NOT NULL,
    REWARD_CODE VARCHAR(50),
    REWARD_NAME VARCHAR(100),
    REWARD_ALIAS VARCHAR(100),
    REWARD_ICON VARCHAR(500),
    STOCK INT DEFAULT 0,
    MAX_CLAIM_TIMES INT DEFAULT 1,
    OCCUPY_WIN_COUNT CHAR(1) DEFAULT 'Y',
    CONDITION_JSON LONGTEXT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 9. 分享配置
CREATE TABLE SHARE_CONFIG (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACTIVITY_ID BIGINT NOT NULL UNIQUE,
    SHARE_TITLE VARCHAR(100),
    SHARE_DESC VARCHAR(300),
    SHARE_ICON VARCHAR(500)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 10. 系统用户表
CREATE TABLE SYS_USER (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    USERNAME VARCHAR(50) NOT NULL UNIQUE,
    PASSWORD VARCHAR(200) NOT NULL,
    REAL_NAME VARCHAR(50),
    DEPT_NAME VARCHAR(100),
    STATUS CHAR(1) DEFAULT 'Y'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 11. 奖励领取记录
CREATE TABLE REWARD_CLAIM_RECORD (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACTIVITY_ID BIGINT NOT NULL,
    CUSTOMER_ID VARCHAR(50),
    PHONE VARCHAR(20),
    REAL_NAME VARCHAR(50),
    REWARD_CODE VARCHAR(50),
    REWARD_NAME VARCHAR(100),
    CLAIM_TIME DATETIME DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

